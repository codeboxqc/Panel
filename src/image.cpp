bool loadImage(const char* path, uint8_t* buffer, size_t bufSize) {
    File file = SPIFFS.open(path, "r");
    if (!file) {
        Serial.println("Failed to open file for reading");
        return false;
    }

    size_t bytesRead = file.read(buffer, bufSize);
    file.close();

    if (bytesRead != bufSize) {
        Serial.printf("Read %d bytes, expected %d\n", bytesRead, bufSize);
        return false;
    }

    Serial.printf("Loaded %d bytes from %s\n", bytesRead, path);
    return true;
}


